package com.bywin.design_patterns.chapater5;

/**
 * 具体的电灯类
 * created by yeric on 2019/8/13
 */
public class Light {
    /**
     * 开灯方法
     */
    public void lightOn() {
        System.out.println("灯打开了！！");
    }

    /**
     * 关灯方法
     */
    public void lightOff() {
        System.out.println("灯关上了！！");
    }
}
